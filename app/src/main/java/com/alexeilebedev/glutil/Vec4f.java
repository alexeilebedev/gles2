package com.alexeilebedev.glutil;


// x,y,z,w vector
public class Vec4f {
    public float _x,_y,_z,_w;

    public Vec4f() {
        _x=0.f;
        _y=0.f;
        _z=0.f;
        _w=0.f;
    }
    public Vec4f(float x, float y, float z, float w) {
        _x=x;
        _y=y;
        _z=z;
        _w=w;
    }
}
